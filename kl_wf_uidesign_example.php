<p>據《打造最強網頁UI/UX設計腦》一書所述，設計最重要的就是「一眼就能看懂」這件事，它之目的在於整理資訊與傳遞資訊，所以無論多麼炫目的視覺設計，若使用者無法快速理解就不具任何意義。如何令使用者「減少動腦思考及計算」遂成為相當注重的課題之一，尤其對智慧型手機或平板電腦這類需要手動操作的行動裝置而言，其重要性更是不可輕忽。</p>
