<p>「格式塔理論」（Gestalt Theory）是現代認知主義學習理論的先驅，起源於 20 世紀初的德國，是德文「Gestalt」的直譯，意即「模式、形狀、形式」等，或者說是「動態的整體（dynamic wholes）」。此理論主張人腦的運作原理是整體性的，例如人們對花的認知是包含對其過去的整體印象及經驗，而非單憑其形狀、顏色、大小等個別感官資訊；抑或者單聽「Do 」這個音符不會意識到是什麼曲子，但當耳朵接受到一段完整的熟悉旋律時，就得以判斷出當下聽到的是什麼歌。</p>
<figure>
    <img src="images/pic/knowledge/gestalt-1.jpg">
    <figcaption>如果將分散的圓形稍作排列，人腦很難不會不由自主地將它們視為一個群體。</figcaption>
</figure>
<br>
<h2>格式塔心理學理論的完形法則：</h2>
<ol class="decimal">
    <li>Proximity（相近）：距離接近的各部分趨於組成形體。</li>
    <li>Similarity（相似）：在某方面相似的各部分趨於組成形體。</li>
    <li>Closure（封閉）：彼此相屬、構成封閉實體的各部分趨於組成形體。</li>
    <li>Continuity（連續）：透過非常細微的共性將兩個不同環節的部分連接成一個群體，例如橫線或是葉脈。</li>
    <li>Simplicity（簡單）：具有對稱、規則、平滑的簡單圖形特徵的各部分趨於組成整體。</li>
</ol>
<p>以上是「格式塔理論」的大略記述，但大概已可理解它始終存在於我們的日常感知，當然網頁設計也不例外，接著來看看一些格式塔之於網頁設計的經典案例。</p>
