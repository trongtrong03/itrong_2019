<p>User Experience，也就是 UX，中文譯為「使用者經驗」，顧名思義，指的是使用者從特定產品或服務裡，透過人機互動獲得的五感體驗。如果說 UI 是實體產品的觀感呈現，那麼 UX 則是使用者自該項產品「無形」的使用經驗。因此，UX 設計師的工作，主要在於站在使用者的角度，思考如何幫助他們從某產品使用上得到良好感受，優化產品設計的同時，並解決操作過程可能會遇到的種種疑難雜症。</p>
<figure>
    <img src="images/pic/knowledge/ux-1.jpg">
</figure>
<h2>UX 設計必須思考的問題：</h2>
<ol class="decimal">
    <li>想要展示資訊的哪些方面？</li>
    <li>想要讓使用者感受到什麼？</li>
    <li>想要讓使用者獲得什麼益處？</li>
    <li>想要讓使用者對哪些資訊印象深刻？</li>
    <li>想要令使用者思維或行動產生多少影響？</li>
    <li>如何使他們未來再次使用你的產品？</li>
</ol>
<h2>UX 設計師最好具備這些能力：</h2>
<ol class="decimal">
    <li>同理心（換位思考）。</li>
    <li>觀察力。</li>
    <li>表達與溝通能力。</li>
    <li>耐心。</li>
</ol>
