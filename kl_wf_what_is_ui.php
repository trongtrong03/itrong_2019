<p>User Interface 中文翻譯為「使用者介面」，簡稱 UI，倘若要以專業術語來解釋，UI 是指系統和用戶之間進行互動和資訊交換的媒介。舉凡電腦應用軟體、手機介面、汽機車儀表板...等等，皆屬於使用者介面一部分，我們會驚訝原來 UI 在日常生活中扮演舉足輕重的角色，好的使用者介面能幫助使用者更簡單地進行操作、清楚地獲取需要資訊，或是能更輕鬆學會陌生軟體的使用方式，也因為 UI 應用是如此地頻繁，UI Designer（使用者界面設計師）成為就業市場上最搶手的職業之一。</p>
<p>UI 設計師的思考方向為「如何做好產品呈現」，著重具體功能與視覺安排方面的設計，UI 囊括應用範圍非常廣泛，移動裝置、遊戲、影音多媒體、廣告、工業、交通、通訊、醫療...族繁不及備載，因此，UI 設計師通常也知悉平面設計，但平面設計師卻不見得擅長 UI 設計。</p>
