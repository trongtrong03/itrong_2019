<p>UI 設計的重點：</p>
<ol class="decimal">
    <li>簡單且清楚。</li>
    <li>容易學習、預測、熟悉、分析。</li>
    <li>操作界面的直覺性、一致性。</li>
    <li>界面彈性與適應性 。</li>
    <li>多樣性、完善之功能。</li>
    <li>充足的操作範本及輔助。</li>
    <li>容易或不需記憶。</li>
    <li>容許使用者修改部份設定以儘量符合使用者個人的心智模型。</li>
    <li>友善性（以使用者的邏輯來思考操作方法）。</li>
    <li>容許使用者犯錯。</li>
    <li>低錯誤。</li>
    <li>整體美感。</li>
</ol>
