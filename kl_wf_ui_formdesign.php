<!-- start -->
<p>如同紙本問券或資料填寫，網站也有許多功能需要使用者輸入表單內容，然而，過多的表單項目欄位，反會增加使用者的負擔，甚至產生抗拒、放棄的想法。當然，這並非意味著網站應該放棄填寫表單，而是設計師必須站在使用者的角度思考，這份表單真正要傳達、獲取的資訊是什麼。</p>
<p>以下列舉讓使用者方便輸入表單的幾個設計要點：</p>
<ol class="decimal">
    <li>將重點放在顧客，而非製作者或營運商本身。</li>
    <li>精簡表單欄位，撇去多餘的表單項目。</li>
    <li>在「必要」輸入的欄位增加符號或文字提示。</li>
    <li>顧慮不想提供「本名」的使用者欄位設計。</li>
    <li>考慮該表單某些欄位的必要性（例如：性別、年齡）。</li>
    <li>將輸入格式（全形半形）交由使用者決定。</li>
    <li>下拉式清單或是選取方塊/按鈕的抉擇（選項多可用清單，反之則較傾向單選按鈕）。</li>
    <li>設計表單錯誤的顯示畫面（文字輸入錯誤、驗證碼錯誤、漏填必要欄位...等）。</li>
</ol>
