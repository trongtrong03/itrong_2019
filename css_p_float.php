<p>看完上方的範例會發現，當子元素設置靠左或靠右的 <em>float</em> 屬性時，因其浮動化的特性，高度較高的子元素會直接溢出父元素外，假如對排版不夠熟悉，很容易就會遭遇版型跑掉的情況，若要讓父元素完全包覆浮動狀態下的子元素，可以透過新增另一個子元素並設置 <em>clear</em> 屬性來解決浮動的問題。</p>
